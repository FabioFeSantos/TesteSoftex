-- Entregas por trimestre
SELECT ANO_ENTREGA, TRIMESTRE_ENTREGA, COUNT(*) AS TOTAL
FROM EntregasProjetos
GROUP BY ANO_ENTREGA, TRIMESTRE_ENTREGA
ORDER BY ANO_ENTREGA, TRIMESTRE_ENTREGA;
