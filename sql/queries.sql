
-- Projetos entregues por ano
SELECT ANO_ENTREGA, COUNT(*) AS QTDE_ENTREGUES
FROM EntregasProjetos
WHERE DATA_FIM_ENTREGA IS NOT NULL
GROUP BY ANO_ENTREGA
ORDER BY ANO_ENTREGA;

